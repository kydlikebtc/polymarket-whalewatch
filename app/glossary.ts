// Prefix lookup: WalletTag.key → hover tip / full entry.
export function walletTagEntry(key: string): WalletTagEntry | undefined {
  return WALLET_TAGS.find((e) => key.startsWith(e.keyPrefix));
}
export const walletTagTip = (key: string): string =>
  walletTagEntry(key)?.tip ?? "";

